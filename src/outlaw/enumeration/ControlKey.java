package outlaw.enumeration;

/** ControlKey enum:
 * A játékoshoz tartozó tevékenységek.
 */
public enum ControlKey {
    LEFT /**< A játékos balra megy. */,
    RIGHT /**< A játékos jobbra megy. */,
    UP /**< A játékos felfele megy. */,
    DOWN /**< A játékos lefele megy. */,
    FIRE /**< A játékos lő. */
}
